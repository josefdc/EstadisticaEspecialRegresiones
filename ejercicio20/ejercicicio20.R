# Coeficiente de correlación
r <- 0.94

# Cuadrado del coeficiente de correlación (coeficiente de determinación)
r_sq <- r^2

# Porcentaje de la varianza que queda sin explicar
porcentaje_varianza_sin_explicar <- (1 - r_sq) * 100

# Imprimir el resultado
print(paste("El porcentaje de la varianza que queda sin explicar es:", porcentaje_varianza_sin_explicar))
